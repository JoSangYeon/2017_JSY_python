t.left(90)
t.circle(150, 180)
t.penup()
t.goto(-350, 150)
t.seth(0)
t.pencolor("black")
t.pendown()
t.fd(700)
t.rt(90)
t.fd(50)
t.rt(90)
t.fd(700)
t.rt(90)
t.fd(50)
t.penup()
t.rt(180)
